my_str = "hello world"

capitalized_str = my_str.capitalize()
print(capitalized_str)

upper_str = my_str.upper()
print(upper_str)

lower_str = my_str.lower()
print(lower_str)

replaced_str = my_str.replace('hello', 'hi')
print(replaced_str)

split_str = my_str.split('\n')
print(split_str)

joined_str = ' '.join(my_str)
print(joined_str)

striped_str = my_str.strip()
print(striped_str)

start_str = my_str.startswith('hello')
print(start_str)

end_str = my_str.endswith('universe')
print(end_str)

finding_str = my_str.find('world')
print(finding_str)

counted_str = my_str.count('o')
print(counted_str)

is_upper_str = my_str.isupper()
print(is_upper_str)

is_lower_str = my_str.islower()
print(is_lower_str)

titled_str = my_str.title()
print(titled_str)
